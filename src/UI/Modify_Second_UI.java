    private void seeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_seeButtonActionPerformed
        // TODO add your handling code here:
        Iterator it = null;

        switch (this.fileType) {

            case HOT:
                it = information.getHotOnes().entrySet().iterator();

                jTextArea1.setText(this.jTextArea1.getText() +
                    "******************************************************\n" +
                    "*********************  HOT ONES  *********************\n" +
                    "******************************************************\n" );

                break;

            case LOVED:
                it = information.getLovedOnes().entrySet().iterator();

                jTextArea1.setText(this.jTextArea1.getText() +
                    "******************************************************\n" +
                    "********************  LOVED ONES  ********************\n" +
                    "******************************************************\n" );

                break;

            case HATED:
                it = information.getHatedOnes().entrySet().iterator();

                jTextArea1.setText(this.jTextArea1.getText() +
                    "******************************************************\n" +
                    "********************  HATED ONES  ********************\n" +
                    "******************************************************\n" );

                break;

            case COMBOS:
                it = information.getPersonalCombos().entrySet().iterator();

                jTextArea1.setText(this.jTextArea1.getText() +
                    "******************************************************\n" +
                    "**********************  COMBOS  **********************\n" +
                    "******************************************************\n" );

                break;

            default:
                System.out.println ("Unknow file type to load (fatal error)");
                System.out.println ("No changes has been made");
                break;
        }

        if (this.fileType.equals(fileType.COMBOS)) {
            if (it.hasNext()) {
                while (it.hasNext()) {
                    Map.Entry pair = (Map.Entry)it.next();
                    //System.out.println(pair.getKey() + ":" + pair.getValue());
                    jTextArea1.setText(this.jTextArea1.getText() + "            " + 
                        pair.getKey() + ".: " + pair.getValue() + "\n" );
                }
            }
            else {
                jTextArea1.setText(this.jTextArea1.getText() + "            >> No information avaiable\n");
            }   
        }
        else {
            if (it.hasNext()) {
                while (it.hasNext()) {
                    Map.Entry pair = (Map.Entry)it.next();
                    //System.out.println(pair.getKey() + ":" + pair.getValue());
                    jTextArea1.setText(this.jTextArea1.getText() + "            " + pair.getKey() + " --> [" +
                            pair.getValue() +"]\n" );
                }
            }
            else {
                jTextArea1.setText(this.jTextArea1.getText() + "            >> No information avaiable\n");
            }
        }

        jTextArea1.setText(this.jTextArea1.getText() + "\n\n" );
        
    }//GEN-LAST:event_seeButtonActionPerformed

