    private void modifySecondDeleteAllBUttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modifySecondDeleteAllBUttonActionPerformed
        // TODO add your handling code here:
        String message = null;
        String file = null;
        String fullOutputPath = null;
        
        switch (this.fileType) {

            case HOT:
                message = "Do you want to empty the HOT ONES file?";
                file = "Hot ones file";
                fullOutputPath = path.getMainPath() + path.getPersonalPath() + path.getHotOnesFile();
                break;
                
            case LOVED:
                message = "Do you want to empty the LOVED ONES file?";
                file = "Loved ones file";
                fullOutputPath = path.getMainPath() + path.getPersonalPath() + path.getLovedOnesFile();
                break;
                
            case HATED:
                message = "Do you want to empty the HATED ONES file?";
                file = "Hated ones file";
                fullOutputPath = path.getMainPath() + path.getPersonalPath() + path.getHatedOnesFile();
                break;

            case COMBOS:
                message = "Do you want to empty the COMBOS file?";
                file = "Combos file";
                fullOutputPath = path.getMainPath() + path.getPersonalPath() + path.getPersonalCombosFile();
                break;

            default:
                System.out.println ("Unknow file type to load (fatal error)");
                System.out.println ("No changes has been made");
                break;
        }


        int selectedOption = JOptionPane.showConfirmDialog(null,
                                  message, // Message of the window
                                  "Deleting file", // Message of the header
                                  JOptionPane.YES_NO_OPTION);

        if (selectedOption == JOptionPane.YES_OPTION) {

            // First we clear the file
            PrintWriter writer = null;
            try {
                writer = new PrintWriter(fullOutputPath);
            } catch (FileNotFoundException ex) {
                Logger.getLogger(Modify_Second_UI.class.getName()).log(Level.SEVERE, null, ex);
            }
            writer.close();

            // And then, the map from Information class
            switch (this.fileType) {

                case HOT:
                    this.information.getHotOnes().clear();
                    break;

                case LOVED:
                    this.information.getLovedOnes().clear();
                    break;

                case HATED:
                    this.information.getHatedOnes().clear();
                    break;

                case COMBOS:
                    this.information.getPersonalCombos().clear();
                    break;

                default:
                    System.out.println ("Unknow file type to load (fatal error)");
                    System.out.println ("No changes has been made");
                    break;
            }
            
            jTextArea1.setText(this.jTextArea1.getText() + " **********************************************\n"
                 + "            " + file.toUpperCase() + " has been cleared\n"
                 + " **********************************************\n\n" );

        }
   
    }//GEN-LAST:event_modifySecondDeleteAllBUttonActionPerformed

